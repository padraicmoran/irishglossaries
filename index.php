<h1>Early Irish Glossaries Database</h1>

<p>A resource by 
<a href="http://www.asnc.cam.ac.uk/people/Paul.Russell/" target="_blank">Paul Russell</a>, 
<a href="https://qub.academia.edu/SharonArbuthnot" target="_blank">Sharon Arbuthnot</a>, 
<a href="http://www.pmoran.ie/" target="_blank">Pádraic Moran</a>,
funded by the <a href="http://www.ahrc.ac.uk/" target="_blank">Arts and Humanities Research Council</a>.
</p>


<p>The early Irish glossaries <i><a href="texts.php?versionID=9">Sanas Cormaic</a></i> (Cormac’s Glossary), <a href="texts.php?versionID=10">O’Mulconry’s Glossary</a> (<i>De origine scoticae linguae</i>),
<i><a href="texts.php?versionID=2">Dúil Dromma Cetta</a></i> (the Collection of Druim Cett) and the shorter texts
<a href="texts.php?versionID=6"><i>Loman</i></a> and <a href="texts.php?versionID=17"><i>Irsan</i></a>
are important resources for our understanding of the literary and cultural environment of medieval
Ireland. These inter-related texts, compiled from the seventh or eighth century onwards, comprise several 
thousand headwords followed by commentary that ranges from single-word
explanations to extended narratives running to several pages. </p>

<p>The <b>Early Irish Glossaries Project</b> was a three-year project (2006–2009) based in the 
<a href="http://www.asnc.cam.ac.uk" target="_blank">Department of Anglo-Saxon, Norse and Celtic</a> at the University of Cambridge. 
The project produced this digital resource and a series of articles on the texts and contexts of early Irish glossaries (see <a href="biblio.php">Bibliography</a>).
An edition of both <a href="http://www.brepols.net/Pages/ShowProduct.aspx?prod_id=IS-9782503581798-1" target="_blank">O'Mulconry's Glossary and <i>Irsan</i></a> was published in 2019.  
Editions of other glossaries are in progress.
</p>

<p>This digital resource provides:  
</p> 

<ul>
<li><a href="texts.php">Transcriptions</a> for most manuscript versions, with links to manuscript images and dictionary resources.</li>
<li>Tools for generating <a href="concordances.php">concordances</a>.</li>
<li>Search tools: simple search (see box in top right) and <a href="search.php">detailed search</a> options.</li>
<li>A <a href="biblio.php">bibliography</a> (see also <a href="abbr.php">abbreviations</a>).</li>
</ul>

